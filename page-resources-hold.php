<h2>Tools</h2>

<span>To help you deconstruct the election, we've pulled together a range of free tools that make it easy to create, edit and share video.</span>

<h3><a href="https://archive.org/pop/editor.html">Popcorn Maker</a></h3>
<img src="#">
<span><strong>What is it:</strong> A web-based video editor that lets you use media from across the web. You can use this to make video essays, video sequences, and montages.</span>
<span><strong>Getting started:</strong> You can <a href="https://archive.org/pop/editor.html">start a new project</a>, <a href="#">remix a sample project</a>, or <a href="#">view video tutorials</a> here. </span>

<h3><a href="http://election.hyperaud.io">Hyperaudio Pad</a></h3>
<img src="#">
<span><strong>What is it:</strong> a simple, web-based tool that lets you edit video as you would edit text--by copy-pasting.</span>
<span><strong>Getting started:</strong> Visit <a href="http://election.hyperaud.io">election.hyperaud.io</a> to create a new project. We have collected <a href="#content">core content</a> including debates, rallies and speeches for you to work with. Check out the video tutorial <a href="#">here</a>.</span>

<h3><a href="http://giphy.com/create/gifmaker
">Giphy Gifmaker</a></h3>
<img src="#">
<span><strong>What is it:</strong> A fast and easy way to create highly sharable animated GIFs. </span>
<span><strong>Getting started:</strong>visit <a href="http://giphy.com/create/gifmaker">giphy.com/create/gifmaker</a>.
 </span>

<h3><a href="http://antiboredom.github.io/videogrep/
">Videogrep</a></h3>
<img src="#">
<span><strong>What is it:</strong> a powerful tool that will create supercuts from video transcripts. Available for Mac computers and there's also a more advanced version that uses the command line.</span>
<span><strong>Getting started:</strong> visit <a href="#">the install page</a> to get Videogrep. Check out this <a href="#">tutorial</a>, and when you're ready, get to work with some <a href="#content"> sample content</a> that we have collected for you.</span>

<h3><a href="http://tv.archive.org
">Internet Archive: TV News Archive</a></h3>
<img src="#">
<span><strong>What is it:</strong> A library archive of TV news, which enables you to search captions and analyze public broadcasts for research purposes. </span>
<span><strong>Getting started:</strong> Visit <a href="http://tv.archive.org
">tv.archive.org</a> to search the TV News Archive. You may also be interested in the <a href="http://politicaladarchive.org">Political Ad Archive</a>.
</span>

